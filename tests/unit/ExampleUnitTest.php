namespace boxheadsoundcloud\soundcloudsynctests\unit;

use Codeception\Test\Unit;
use UnitTester;
use Craft;
use boxheadsoundcloud\soundcloudsync\SoundcloudSync;

/**
 * ExampleUnitTest
 *
 *
 * @author    Boxhead
 * @package   SoundcloudSync
 * @since     1.0.0
 */
class ExampleUnitTest extends Unit
{
    // Properties
    // =========================================================================

    /**
     * @var UnitTester
     */
    protected $tester;

    // Public methods
    // =========================================================================

    // Tests
    // =========================================================================

    /**
     *
     */
    public function testPluginInstance()
    {
        $this->assertInstanceOf(
            SoundcloudSync::class,
            SoundcloudSync::$plugin
        );
    }

    /**
     *
     */
    public function testCraftEdition()
    {
        Craft::$app->setEdition(Craft::Pro);

        $this->assertSame(
            Craft::Pro,
            Craft::$app->getEdition()
        );
    }
}
